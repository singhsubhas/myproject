var B=document.querySelector('.B')
    B.addEventListener('mouseover',()=>{
    var popup=document.querySelector('.popup')
    popup.style.display='block'
})
B.addEventListener('mouseleave',()=>{
    setTimeout(()=>{
    var popup=document.querySelector('.popup')
    popup.style.display='none'
},2000)
})


